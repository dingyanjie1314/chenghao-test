# 定义元组
# 多个数据元组
t1 = (1, 2, 304)
print(t1)
# 单个元组
t2 = (1,)
print(t2)
t3 = (1)
print(t3)

# 查找
# 下标查找
# index(): 查找某个数据, 如果数据存在返回对应的下标, 否则报错
t1 = ("a", "s", "d", "f","a")
print(t1.index("d"))
#count(): 统计某个数据在当前元组出现的次数
t1 = ("a", "s", "d", "f","a")
print(t1.count("a"))
#len(): 统计元组中数据的个数
t1 = ("a", "s", "d", "f","a")
print(len(t1))
# 元组内的直接数据如果修改则立即报错,但是如果元组里面有列表, 修改列表里面的数据则是支持的, 故自觉很重要
t1=("a","b",["a","b"])
#t1[0]="aa"#报错
t1[2][0]="aa"
print(t1)
